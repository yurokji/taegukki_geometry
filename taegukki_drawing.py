대각선_길이 = math.sqrt(가로_길이 * 가로_길이  + 세로_길이 * 세로_길이)
반대각선_길이 = 대각선_길이 / 2

cosine = 반가로_길이/반대각선_길이
sine = math.sqrt(1 - cosine * cosine)
회전라디안각 = math.acos(cosine)
회전각 = math.degrees(회전라디안각)




# 파랑 돌기가 위치할 원을 스케치한다
파랑돌기_x위치 = 반지름 * cosine
파랑돌기_y위치 = 반지름 * -sine
p.goto(파랑돌기_x위치,파랑돌기_y위치)
