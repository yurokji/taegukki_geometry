반지름 = 200
지름 = 반지름 * 2
가로_길이 = 지름 * 3
세로_길이 = 지름 * 2
반가로_길이 = 가로_길이 / 2
반세로_길이 = 세로_길이 / 2


p = turtle.Turtle()
p.speed(1)

# 태극기의 깃발이 그려질 곳을 굷은 선으로 긋는다
p.pensize(3)
